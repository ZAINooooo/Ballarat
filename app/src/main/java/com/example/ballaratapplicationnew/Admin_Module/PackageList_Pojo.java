package com.example.ballaratapplicationnew.Admin_Module;

public class PackageList_Pojo {

    private String id;

    private String pkg_name;
    private String pkg_image;
    private String pkg_price;

    public String getId() {
        return id;
    }
    public void setId (String id)
    {
        this.id = id;
    }

    public String getPkg_name() {
        return pkg_name;
    }
    public void setPkg_name (String pkg_name)
    {
        this.pkg_name = pkg_name;
    }
    public String getPkg_price() {
        return pkg_price;
    }
    public void setPkg_price (String pkg_price)
    {
        this.pkg_price = pkg_price;
    }
    public String getPkg_image() {
        return pkg_image;
    }
    public void setPkg_image (String pkg_image)
    {
        this.pkg_image = pkg_image;
    }


}
